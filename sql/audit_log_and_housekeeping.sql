  elsif tg_op = 'DELETE' then
    perform public.log_app_event(
      old.user_profile_id,
      'recommendation_deleted',
      'recommendations',
      old.id,
      jsonb_build_object(
        'recommendation_run_id', old.recommendation_run_id,
        'strategy_type', old.strategy_type
      )
    );
    return old;
  end if;

  return null;
end;
$$;

