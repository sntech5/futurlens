returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    perform public.log_app_event(
      new.user_profile_id,
      'recommendation_created',
      'recommendations',
      new.id,
      jsonb_build_object(
        'recommendation_run_id', new.recommendation_run_id,
        'strategy_type', new.strategy_type,
        'top_suburbs_count', jsonb_array_length(coalesce(new.top_suburbs, '[]'::jsonb))
