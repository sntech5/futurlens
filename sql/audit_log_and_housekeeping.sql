-- ---------------------------------------------------------------------------
-- 5) Duplicate cleanup for recommendations table
--    Keeps latest recommendation per run_id, removes older duplicates.
-- ---------------------------------------------------------------------------

create or replace function public.cleanup_duplicate_recommendations_per_run(
  p_dry_run boolean default true
)
returns table(action text, affected_rows bigint)
language plpgsql
as $$
declare
  v_rows bigint;
begin
  with dupes as (
    select id
    from (
      select
        r.id,
        row_number() over (
          partition by r.recommendation_run_id
          order by r.created_at desc, r.id desc
        ) as rn
      from public.recommendations r
    ) t
    where t.rn > 1
  )
  select count(*) into v_rows from dupes;

  if p_dry_run then
    return query select 'dry_run_duplicate_recommendations'::text, coalesce(v_rows, 0);
    return;
  end if;

  delete from public.recommendations r
  where r.id in (
    select id
    from (
      select
        r2.id,
        row_number() over (
          partition by r2.recommendation_run_id
          order by r2.created_at desc, r2.id desc
        ) as rn
      from public.recommendations r2
    ) z
    where z.rn > 1
  );

  get diagnostics v_rows = row_count;
  return query select 'deleted_duplicate_recommendations'::text, coalesce(v_rows, 0);
end;
$$;

-- ---------------------------------------------------------------------------
-- 6) Housekeeping cleanup for old duplicate-criteria runs
--    Deletes runs older than threshold while keeping newest N per criteria.
--    Criteria signature: user_profile_id + strategy_type + input_budget + max_oop.
-- ---------------------------------------------------------------------------

create or replace function public.cleanup_old_recommendation_runs(
  p_keep_latest_per_signature integer default 1,
  p_delete_older_than_days integer default 30,
  p_dry_run boolean default true
)
returns table(action text, affected_rows bigint)
language plpgsql
as $$
declare
  v_candidate_runs bigint;
  v_deleted_recommendations bigint;
  v_deleted_runs bigint;
begin
  if p_keep_latest_per_signature < 1 then
    raise exception 'p_keep_latest_per_signature must be >= 1';
  end if;

  with ranked as (
    select
      rr.id,
      row_number() over (
        partition by rr.user_profile_id, rr.strategy_type, rr.input_budget, rr.max_out_of_pocket
        order by rr.created_at desc, rr.id desc
      ) as rn
    from public.recommendation_runs rr
    where rr.created_at < now() - make_interval(days => p_delete_older_than_days)
      and coalesce(rr.run_status, 'completed') <> 'pending'
  ),
  candidates as (
    select id
    from ranked
    where rn > p_keep_latest_per_signature
  )
  select count(*) into v_candidate_runs from candidates;

  if p_dry_run then
    return query select 'dry_run_candidate_runs'::text, coalesce(v_candidate_runs, 0);
    return;
  end if;

  -- Delete child rows first.
  delete from public.recommendations r
  where r.recommendation_run_id in (
    with ranked as (
      select
        rr.id,
        row_number() over (
          partition by rr.user_profile_id, rr.strategy_type, rr.input_budget, rr.max_out_of_pocket
          order by rr.created_at desc, rr.id desc
        ) as rn
      from public.recommendation_runs rr
      where rr.created_at < now() - make_interval(days => p_delete_older_than_days)
        and coalesce(rr.run_status, 'completed') <> 'pending'
    )
    select id from ranked where rn > p_keep_latest_per_signature
  );
  get diagnostics v_deleted_recommendations = row_count;

  delete from public.recommendation_runs rr
  where rr.id in (
    with ranked as (
      select
        rr2.id,
        row_number() over (
          partition by rr2.user_profile_id, rr2.strategy_type, rr2.input_budget, rr2.max_out_of_pocket
          order by rr2.created_at desc, rr2.id desc
        ) as rn
      from public.recommendation_runs rr2
      where rr2.created_at < now() - make_interval(days => p_delete_older_than_days)
        and coalesce(rr2.run_status, 'completed') <> 'pending'
    )
    select id from ranked where rn > p_keep_latest_per_signature
  );
  get diagnostics v_deleted_runs = row_count;

  return query select 'deleted_recommendations'::text, coalesce(v_deleted_recommendations, 0);
  return query select 'deleted_runs'::text, coalesce(v_deleted_runs, 0);
end;
$$;

