-- Reference: Docs/audit_log_and_housekeeping_guide.md
-- Purpose: add usage audit logging + safe cleanup utilities for recommendation runs/results.
-- Safe default: cleanup functions support dry-run mode.

-- ---------------------------------------------------------------------------
-- 1) Audit log table
-- ---------------------------------------------------------------------------

create table if not exists public.app_audit_log (
  id bigint generated by default as identity primary key,
  event_time timestamptz not null default now(),
  actor_user_profile_id uuid null,
  event_name text not null,
  entity_table text not null,
  entity_id uuid null,
  metadata jsonb not null default '{}'::jsonb
);

create index if not exists idx_app_audit_log_event_time
  on public.app_audit_log (event_time desc);

create index if not exists idx_app_audit_log_actor
  on public.app_audit_log (actor_user_profile_id, event_time desc);

create index if not exists idx_app_audit_log_event_name
  on public.app_audit_log (event_name, event_time desc);

-- ---------------------------------------------------------------------------
-- 2) Generic logging helper
-- ---------------------------------------------------------------------------

create or replace function public.log_app_event(
  p_actor_user_profile_id uuid,
  p_event_name text,
  p_entity_table text,
  p_entity_id uuid,
  p_metadata jsonb default '{}'::jsonb
)
returns void
language plpgsql
as $$
begin
  insert into public.app_audit_log (
    actor_user_profile_id,
    event_name,
    entity_table,
    entity_id,
    metadata
  )
  values (
    p_actor_user_profile_id,
    p_event_name,
    p_entity_table,
    p_entity_id,
    coalesce(p_metadata, '{}'::jsonb)
  );
end;
$$;

-- ---------------------------------------------------------------------------
-- 3) Triggers for recommendation_runs and recommendations
-- ---------------------------------------------------------------------------

create or replace function public.trg_log_recommendation_runs()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    perform public.log_app_event(
      new.user_profile_id,
      'recommendation_run_created',
      'recommendation_runs',
      new.id,
      jsonb_build_object(
        'strategy_type', new.strategy_type,
        'input_budget', new.input_budget,
        'max_out_of_pocket', new.max_out_of_pocket,
        'run_status', new.run_status
      )
    );
    return new;
  elsif tg_op = 'UPDATE' then
    perform public.log_app_event(
      new.user_profile_id,
      'recommendation_run_updated',
      'recommendation_runs',
      new.id,
      jsonb_build_object(
        'old_run_status', old.run_status,
        'new_run_status', new.run_status
      )
    );
    return new;
  elsif tg_op = 'DELETE' then
    perform public.log_app_event(
      old.user_profile_id,
      'recommendation_run_deleted',
      'recommendation_runs',
      old.id,
      jsonb_build_object(
        'strategy_type', old.strategy_type,
        'input_budget', old.input_budget,
        'max_out_of_pocket', old.max_out_of_pocket
      )
    );
    return old;
  end if;

  return null;
end;
$$;

drop trigger if exists trg_recommendation_runs_audit on public.recommendation_runs;
create trigger trg_recommendation_runs_audit
after insert or update or delete
on public.recommendation_runs
for each row
execute function public.trg_log_recommendation_runs();

